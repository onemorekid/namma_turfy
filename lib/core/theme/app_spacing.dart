/// Spacing scale — base unit 4 px.
/// Use these constants for padding, margin, and gap values.
class AppSpacing {
  AppSpacing._();

  static const double xs = 4;
  static const double sm = 8;
  static const double md = 16;
  static const double lg = 24;
  static const double xl = 32;
  static const double xxl = 48;

  /// Standard horizontal screen padding (16 px).
  static const double screenH = md;

  /// Standard card internal padding (16 px).
  static const double cardPad = md;

  /// Gap between list items (12 px).
  static const double listGap = 12;

  /// Bottom CTA button bottom padding above SafeArea (20 px).
  static const double ctaBottom = 20;
}
